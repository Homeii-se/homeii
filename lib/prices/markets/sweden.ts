export const SE_ANNUAL_DATA: AnnualPriceSummary[] = (() => {
  const result: AnnualPriceSummary[] = [];
  const currentYear = new Date().getFullYear();
  const currentMonth = new Date().getMonth() + 1;

  for (const zone of ["SE1","SE2","SE3","SE4"] as ZoneId[]) {
    for (const year of [2020,2021,2022,2023,2024,2025]) {
      const months = SE_MONTHLY_DATA.filter(d => d.zoneId === zone && d.year === year);
      if (!months.length) continue;
      const avg = months.reduce((s, d) => s + d.avgOreKwh, 0) / months.length;
      result.push({
        year,
        zoneId: zone,
        avgOreKwh: Math.round(avg * 100) / 100,
        source: "entsoe",
        partial: year === currentYear && currentMonth < 12,
      });
    }
  }
  return result;
})();
